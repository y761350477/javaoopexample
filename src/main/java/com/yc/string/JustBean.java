package com.yc.string;

public class JustBean {
    private String labelName;
    private int CaseNum;
    private int DataNum;
    private int SuspectNum;

    public String getLabelName() {
        return labelName;
    }

    public void setLabelName(String labelName) {
        this.labelName = labelName;
    }

    public int getCaseNum() {
        return CaseNum;
    }

    public void setCaseNum(int caseNum) {
        CaseNum = caseNum;
    }

    public int getDataNum() {
        return DataNum;
    }

    public void setDataNum(int dataNum) {
        DataNum = dataNum;
    }

    public int getSuspectNum() {
        return SuspectNum;
    }

    public void setSuspectNum(int suspectNum) {
        SuspectNum = suspectNum;
    }
}
